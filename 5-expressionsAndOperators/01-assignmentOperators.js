/*
Assignment Operators
*/

//assignment operator
let x = 5;
console.log(x);

x = x + 1;  // JS will first look at x + 1
console.log(x);

//addition operator
x += 1; // x = x + 1 shorthand 
console.log(x);

//subtraction operator 
x -= 1; // x = x-1
console.log(x);

//multiplication operator 
x *= 1; // x = x * 1 
console.log(x);

//division operator 
x /= 1; // x = x / 1
console.log(x);


console.log(10%3); // 3 goes into 10 3 times evenly, with 1 as a remainder. The % symbol outputs that remainder. 

console.log(7%5);

var x = 5
x = x % 5; // shorthand is x %= 5 
console.log(x % 5);  


//exponential assignment 

let x = 5;
x = x ** 2; // shorthand is x **= 2 
x **= 1
console.log(x);

